
public class BestMovie {
	/*
	 * Write a program to output the 
	 * title of your favorite movie on one line
	 * actor in the movie on the next line
	 */
	public static void main(String[] args) {
	
		// using 3 print statements
		System.out.print("Shrek");
		System.out.print("\n");
		System.out.print("Mike Myers");
		
		System.out.print("\n\n"); // divider
		
		// using 2 printlns
		System.out.println("Shrek 2");
		System.out.println("Eddie Murphy");
		
		System.out.print("\n"); // divider
		
		// concatenation
		System.out.print("Shrek 3" + "\n" + "Cameron Diaz");
		
		System.out.print("\n\n"); // divider
		
		// single string
		System.out.print("Shrek 4\nJustin Timberlake");
				
		
	
	}

}
